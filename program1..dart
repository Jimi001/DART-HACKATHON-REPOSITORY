## Program 1: Display Personal Information
Write a Dart program using variables to display your name, age, school, and a hobby. Print it in one logical sentence.
void main() {
  // Declare and initialize variables
  String name = "John";
  int age = 25;
  String school = "XYZ School";
  String hobby = "playing soccer";

  // Print a sentence using the variables
  print("My name is $name. I am $age years old. I study at $school, and my hobby is $hobby.");
}
